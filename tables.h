class sintable
{
  public:

    sintable() {}

    /**
       Gets the pointer to the array containing the wanted note.
    */
    byte * getPointer(int note) {
      switch (note) {
        case 0:
          return SinB4;
        case 1:
          return SinA4S;
        case 2:
          return SinA4;
        case 3:
          return SinG4S;
        case 4:
          return SinG4;
        case 5:
          return SinF4S;
        case 6:
          return SinF4;
        case 7:
          return SinE4;
        case 8:
          return SinD4S;
        case 9:
          return SinD4;
        case 10:
          return SinC4S;
        case 11:
          return SinC4;
      }
    };

    /**
       Gets the size of the array of the wanted note.
    */
    int getSize(int note) {
      switch (note) {
        case 0:
          return sizeof(SinB4);
        case 1:
          return sizeof(SinA4S);
        case 2:
          return sizeof(SinA4);
        case 3:
          return sizeof(SinG4S);
        case 4:
          return sizeof(SinG4);
        case 5:
          return sizeof(SinF4S);
        case 6:
          return sizeof(SinF4);
        case 7:
          return sizeof(SinE4);
        case 8:
          return sizeof(SinD4S);
        case 9:
          return sizeof(SinD4);
        case 10:
          return sizeof(SinC4S);
        case 11:
          return sizeof(SinC4);
      }
    }

    /**
       Reads from the PROGMEM to get the sin curve points for the wanted note. It then sets the variable table[] located in the SRAM (For faster access).

       Returns the size of the array of the wanted note.
    */
    int getTable(int table[], int sizeOf, int note) {
      byte *finalTable = getPointer(note);

      for (int i = 0; i < sizeOf; i++) {
        table[i] = pgm_read_byte(&finalTable[i]);
      }

      return sizeOf;
    }
};

